// Helper function to get products by category
export const getProductsByCategory = (category, type) => {
  if (category === "featured") return featuredProductsDetail;
  if (category === "trending") return trendingProductsDetail;
  if (category === "sale") return saleProducts;
  if (category === "newSesson") return newSessonProducts;
  if (category === "bags") return bagsProducts;
  if (category === "accessories") return accessoriesProducts;
  if (category === "men") {
    if (type === "shirts") return menShirts;
    if (type === "Jackets") return menJackets;
    if (type === "hat") return menHat;
    if (type === "shoes") return menShoes;
    if (type === "accessories") return menAccessories;
    if (type === "pullover") return menPullover;
    return menShirts;
  }
  if (category === "woman") {
    if (type === "shirts") return womenShirts;
    if (type === "Jackets") return womenJackets;
    if (type === "hat") return womenHat;
    if (type === "shoes") return womenShoes;
    if (type === "accessories") return womenAccessories;
    if (type === "pullover") return womenPullover;
    return womenShirts;
  }
  if (category === "children") {
    if (type === "shirts") return childrenShirts;
    if (type === "Jackets") return childrenJackets;
    if (type === "hat") return childrenHat;
    if (type === "shoes") return childrenShoes;
    if (type === "accessories") return childrenAccessories;
    return childrenShirts;
  }
  return [];
};

// Helper function to get a single product by ID
export const getProductById = (category, type, id) => {
  const products = getProductsByCategory(category, type);
  return products.find(product => +product.id === +id);
};
